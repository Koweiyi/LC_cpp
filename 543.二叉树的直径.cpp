#include<bits/stdc++.h>
using namespace std;
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};
// @lc code=start
/**
 * Definition for a binary tree node.
 */
class Solution {

private:
    int res = -1;
    int deep(TreeNode* root){
        if(!root)
            return 0;
        int L = deep(root->left);
        int R = deep(root->right);
        res = max(res, L + R);

        return max(L, R) + 1;
    }
public:
    int diameterOfBinaryTree(TreeNode* root) {
        if(!root) return 0;
        deep(root);
        return res;   
    }
};
